<template>
    <div>
        <h1>Detail{{ id }}</h1>
        <div v-if="post">
            <h3>{{ post.title }}</h3>
            <p>{{ post.body }}</p>
        </div>
        <div v-else>
            <Spinner></Spinner>
        </div>
    </div>
</template>

<script>

import Spinner from '../components/Spinner'
import getPost from '@/composable/getPost';
export default {
  components: { Spinner },
    props:['id'],
    
    setup (props) {
        
        let {post,error,load} = getPost(props.id)

        load();
        return {post,error}
    }
}
</script>

